package com.egehurturk;

public class Comparison {

    public static void main(String[] args) {
        // comparison operators to compare primitives
        int x = 1;
        int y = 1;
        System.out.println(x == y); // boolean expression
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
    }
}
